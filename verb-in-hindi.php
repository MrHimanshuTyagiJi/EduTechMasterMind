        <div class="container"><h2 id="क्रिया - परिभाषा, भेद, और उदाहरण"><b class="color-content1">क्रिया - परिभाषा, भेद, और उदाहरण</b></h2>
          <div><b class="color-content4" >क्रिया:</b><span style="background-color: var(--background-color); color: var(--text-color);">&nbsp;</span><span style="color: var(--text-color); background-color: var(--background-color);">क्रिया</span><span style="color: var(--text-color); background-color: var(--background-color);">&nbsp;वह शब्द है जो वाक्य में किसी कार्य, घटना या अवस्था का बोध कराता है। क्रिया वाक्य का महत्वपूर्ण भाग होती है और इसके बिना वाक्य का कोई मतलब नहीं होता। यह बताती है कि क्या हो रहा है, क्या किया जा रहा है, या किसी स्थिति में क्या हो रहा है।<br></span>
              <p id="क्रिया की परिभाषा:"><b class="color-content4">क्रिया की परिभाषा:&nbsp;</b><span style="background-color: var(--background-color); color: var(--text-color);">"क्रिया वह शब्द है जो किसी कार्य, घटना या स्थिति का बोध कराता है।" उसे&nbsp;</span><b class="color-content6" >क्रिया</b><span style="background-color: var(--background-color); color: var(--text-color);">&nbsp;कहते हैं।&nbsp;</span></p>
          </div>
          <div><br></div>
          <div><b class="color-content9"><b class="color-content9">उदाहरण-</b></b></div>
          <div>
              <ul>
                  <li>मोहन दौड़ रहा है। (दौड़ रहा है - क्रिया)</li>
                  <li>सीता खाना बना रही है। (बना रही है - क्रिया)</li>
                  <li>वे स्कूल जा रहे हैं। (जा रहे हैं - क्रिया)</li>
                  <li>पक्षी उड़ रहे हैं। (उड़ रहे हैं - क्रिया)</li>
              </ul>
          </div>
          <div>इन वाक्यों में 'दौड़ रहा है', '<b class="color-content6" >बना रही है</b><span style="background-color: var(--background-color); color: var(--text-color);">', '</span><b class="color-content6" >जा रहे हैं</b><span style="background-color: var(--background-color); color: var(--text-color);">', और '</span><b class="color-content6" >उड़ रहे हैं</b><span style="background-color: var(--background-color); color: var(--text-color);">' क्रियाएं हैं जो क्रमशः दौड़ने, बनाने, जाने, और उड़ने की क्रिया का बोध कराती हैं।</span><br></div>
          <div><br></div>
          <div>
              <p id="धातु के भेद:"><b class="color-content5">धातु के भेद:</b></p>
          </div>
          <div><br></div>
          <div>हिंदी व्याकरण में धातु का महत्व अत्यधिक है, क्योंकि यह क्रिया का मूल रूप होता है। धातु के आधार पर ही विभिन्न क्रियाओं का निर्माण होता है। धातु मुख्यतः दो प्रकार की होती हैं: मूल धातु और यौगिक धातु। नीचे इन दोनों भेदों के बारे में विस्तार से समझाया गया है।</div>
          <div><br></div>
          <div>
              <p><b class="color-content6">1. मूल धातु (Primary Root):&nbsp;</b><span style="text-align: left; background-color: var(--background-color); color: var(--text-color);">मूल धातु वह धातु होती है जो स्वतंत्र रूप से उपयोग की जाती है और इसे बनाने के लिए किसी अन्य शब्द की आवश्यकता नहीं होती। यह सबसे सरल रूप होती है और इससे सीधे-सीधे क्रिया का निर्माण होता है।</span></p>
          </div>
          <div><b class="color-content9" ><b class="color-content9">उदाहरण-</b></b><br></div>
          <div>
              <ul>
                  <li>चल (चलना)</li>
                  <li>पढ़ (पढ़ना)</li>
                  <li>देख (देखना)</li>
                  <li>लिख (लिखना)</li>
              </ul>
          </div>
          <div>इन उदाहरणों में "<b class="color-content6" >चल</b><span style="background-color: var(--background-color); color: var(--text-color);">," "</span><b class="color-content6" >पढ़,</b><span style="background-color: var(--background-color); color: var(--text-color);">" "</span><b class="color-content6" >देख</b><span style="background-color: var(--background-color); color: var(--text-color);">," और "</span><b class="color-content6" >लिख</b><span style="background-color: var(--background-color); color: var(--text-color);">" मूल धातुएँ हैं जो सीधे क्रियाओं का निर्माण करती हैं।</span><br></div>
          <div><br></div>
          <div>
              <p><b class="color-content6">2. यौगिक धातु (Compound Root):</b></p>
          </div>
          <div>यौगिक धातु वह धातु होती है जो दो या दो से अधिक शब्दों को मिलाकर बनाई जाती है। यौगिक धातुएं विभिन्न प्रकार से बनाई जा सकती हैं, जिनमें से कुछ प्रमुख प्रकार निम्नलिखित हैं:</div>
          <div><br></div>
          <div>
              <p id="(i) पद-पद योग (Word-Word Combination):"><b class="color-content8">(i) पद-पद योग (Word-Word Combination):&nbsp;</b><span style="text-align: left; background-color: var(--background-color); color: var(--text-color);">जब दो या दो से अधिक शब्दों को मिलाकर क्रिया बनाई जाती है, तो उसे पद-पद योग कहते हैं।</span></p>
          </div>
          <div><b class="color-content9" ><b class="color-content9">उदाहरण-</b></b><br></div>
          <div>
              <ul>
                  <li><span style="background-color: var(--background-color); color: var(--text-color);">गाना-बजाना</span></li>
                  <li>चलना-फिरना</li>
                  <li>हँसना-रोना</li>
              </ul>
          </div>
          <div>इन उदाहरणों में "<b class="color-content9">गाना-बजाना</b>," "<b class="color-content9">चलना-फिरना</b>," और "<b class="color-content9">हँसना-रोना</b>" पद-पद योग द्वारा बनाई गई यौगिक धातुएं हैं।</div>
          <div><br></div>
          <div>
              <p id="(ii) प्रत्यय-योग (Suffix Combination):"><b class="color-content8">(ii) प्रत्यय-योग (Suffix Combination):&nbsp;</b><span style="text-align: left; background-color: var(--background-color); color: var(--text-color);">जब किसी धातु में प्रत्यय (suffix) जोड़कर क्रिया बनाई जाती है, तो उसे प्रत्यय-योग कहते हैं।</span></p>
          </div>
          <div><b class="color-content9"><b class="color-content9">उदाहरण-</b></b></div>
          <div>
              <ul>
                  <li><span style="background-color: var(--background-color); color: var(--text-color);">बोलना (बोल + ना)</span></li>
                  <li>चलाना (चल + आना)</li>
                  <li>खिलाना (खिल + आना)</li>
              </ul>
          </div>
          <div>इन उदाहरणों में "<b class="color-content9">बोलना</b>," "<b class="color-content9">चलाना</b>," और "<b class="color-content9">खिलाना</b>" प्रत्यय-योग द्वारा बनाई गई यौगिक धातुएं हैं।</div>
          <div><br></div>
          <div>
              <p id="(iii) समास-योग (Compound Combination):"><b class="color-content8">(iii) समास-योग (Compound Combination):&nbsp;</b><span style="text-align: left; background-color: var(--background-color); color: var(--text-color);">जब दो या दो से अधिक शब्दों को मिलाकर, उनमें से किसी एक शब्द का लोप करके क्रिया बनाई जाती है, तो उसे समास-योग कहते हैं।<br></span><b class="color-content9" ><b class="color-content9">उदाहरण-</b></b></p>
          </div>
          <div>
              <ul>
                  <li>चलना (चल् + ना)</li>
                  <li>उड़ना (उड्ड + ना)</li>
                  <li>पढ़ना (पढ़् + ना)</li>
              </ul>
          </div>
          <div>इन उदाहरणों में "<b class="color-content9">चलना</b>," "<b class="color-content9">उड़ना</b>," और "<b class="color-content9">पढ़ना</b>" समास-योग द्वारा बनाई गई यौगिक धातुएं हैं।</div>
          <div><br></div>
          <div>
              <p id="यौगिक धातु के अन्य प्रकार:"><b class="color-content7">यौगिक धातु के अन्य प्रकार</b></p>
          </div>
          <div>
              <p id="(i) प्रेरणार्थक क्रिया (Causative Verb):"><b class="color-content8">(i) प्रेरणार्थक क्रिया (Causative Verb):&nbsp;</b><span style="text-align: left; background-color: var(--background-color); color: var(--text-color);">प्रेरणार्थक क्रिया वह क्रिया होती है जो किसी को कोई कार्य करने के लिए प्रेरित करती है। यह क्रिया यह दर्शाती है कि कर्ता किसी अन्य व्यक्ति को किसी कार्य के लिए प्रेरित कर रहा है।</span></p>
          </div>
          <div><b class="color-content9" ><b class="color-content9">उदाहरण-</b></b><br></div>
          <div>
              <ul>
                  <li><span style="background-color: var(--background-color); color: var(--text-color);">सिखाना (सिख + आना)</span></li>
                  <li>बिठाना (बैठ + आना)</li>
                  <li>जगाना (जग + आना)</li>
              </ul>
          </div>
          <div>इन उदाहरणों में "<b class="color-content9">सिखाना</b>," "<b class="color-content9">बिठाना</b>," और "<b class="color-content9">जगाना</b>" प्रेरणार्थक क्रियाएं हैं जो किसी को कार्य करने के लिए प्रेरित करती हैं।</div>
          <div><br></div>
          <div>
              <p id="(ii) नाम धातु (Nominal Verb):"><b class="color-content8">(ii) नाम धातु (Nominal Verb):&nbsp;</b><span style="text-align: left; background-color: var(--background-color); color: var(--text-color);">नाम धातु वह क्रिया होती है जो किसी संज्ञा या विशेषण से बनाई जाती है। यह क्रिया संज्ञा या विशेषण के आधार पर बनती है और उसी से संबंधित कार्य को दर्शाती है।<br></span><b class="color-content9" ><b class="color-content9">उदाहरण-</b></b></p>
          </div>
          <div>
              <ul>
                  <li><span style="background-color: var(--background-color); color: var(--text-color);">गाना (गान से)</span></li>
                  <li>पानी देना (पानी से)</li>
                  <li>बात करना (बात से)</li>
              </ul>
          </div>
          <div>इन उदाहरणों में "<b class="color-content9">गाना</b>," "<b class="color-content9">पानी देना</b>," और "<b class="color-content9">बात करना</b>" नाम धातुएं हैं जो संज्ञा या विशेषण से बनी हैं।</div>
          <div><br></div>
