              <p id="3. भविष्यकाल (Future Tense):"><b class="color-content8">3. भविष्यकाल (Future Tense):&nbsp;</b><span style="text-align: left; background-color: var(--background-color); color: var(--text-color);">भविष्यकाल की क्रिया वह होती है जो भविष्य में होगी। यह क्रिया उस घटना या कार्य को दर्शाती है जो आने वाले समय में संपन्न होगी। यह समय के उस पल को दिखाती है जब क्रिया भविष्य में घटित होगी।</span></p>
