                  <li><span style="background-color: var(--background-color); color: var(--text-color);">रमा रो रही है। (यहाँ कोई कर्म नहीं है)</span></li>
                  <li>पक्षी उड़ रहे हैं। (यहाँ कोई कर्म नहीं है)</li>
                  <li>बच्चा सो रहा है। (यहाँ कोई कर्म नहीं है)</li>
              </ul>
          </div>
          <div>इन वाक्यों में, "<b class="color-content6" >रो रही है</b><span style="background-color: var(--background-color); color: var(--text-color);">," "</span><b class="color-content6" >उड़ रहे हैं</b><span style="background-color: var(--background-color); color: var(--text-color);">," और "</span><b class="color-content6" >सो रहा है</b><span style="background-color: var(--background-color); color: var(--text-color);">" क्रमशः अकर्मक क्रियाएं हैं क्योंकि इन क्रियाओं का कार्य किसी वस्तु या व्यक्ति पर नहीं हो रहा है। ये सभी वाक्य अकर्मक क्रिया के&nbsp;</span><b class="color-content9">उदाहरण</b><span style="background-color: var(--background-color); color: var(--text-color);">&nbsp;हैं क्योंकि इनमें केवल कर्ता का ही उल्लेख है और कोई कर्म नहीं है।</span><br></div>
          <div><br></div>
          <div>
              <div class="pink-background">सकर्मक क्रिया में क्रिया का कार्य किसी वस्तु या व्यक्ति पर होता है, जबकि अकर्मक क्रिया में क्रिया का कार्य किसी वस्तु या व्यक्ति पर नहीं होता है। सकर्मक क्रिया के लिए एक कर्म की आवश्यकता होती है, जबकि अकर्मक क्रिया के लिए नहीं होती है।&nbsp;</div>
