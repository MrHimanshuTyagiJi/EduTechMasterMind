          <div><span style="background-color: var(--background-color); color: var(--text-color);">वाच्य के आधार पर वाक्यों को तीन भागों में विभाजित किया जा सकता है: कर्मवाच्य, कर्तृवाच्य, और भाववाच्य। वाच्य का तात्पर्य वाक्य में किस तत्व को प्रमुखता दी गई है, उससे है।</span><br></div>
