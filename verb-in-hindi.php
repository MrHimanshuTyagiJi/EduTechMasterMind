                  <li>राम ने पुस्तक पढ़ी। (यहाँ "राम" कर्ता है और इसे प्रमुखता दी गई है)</li>
              </ul>
          </div>
          <div>इन वाक्यों में "<b class="color-content6" >लिखी</b><span style="background-color: var(--background-color); color: var(--text-color);">," "</span><b class="color-content6" >बनाया</b><span style="background-color: var(--background-color); color: var(--text-color);">," और "</span><b class="color-content6" >पढ़ी</b><span style="background-color: var(--background-color); color: var(--text-color);">" क्रियाएं कर्तृवाच्य हैं क्योंकि इनमें कार्य का मुख्य प्रभाव कर्ता पर है और कर्ता कार्य को कर रहा है।</span><br></div>
          <div><br></div>
          <div>
              <p id="3. भाववाच्य (Impersonal Voice):"><b class="color-content8">3. भाववाच्य (Impersonal Voice):&nbsp;</b><span style="text-align: left; background-color: var(--background-color); color: var(--text-color);">भाववाच्य वह वाक्य होता है जिसमें भाव या स्थिति पर अधिक महत्व दिया जाता है। इस वाक्य में कर्ता और कर्म दोनों का सीधा उल्लेख नहीं होता बल्कि क्रिया के माध्यम से भाव या स्थिति का बोध होता है। यह वाक्य उस स्थिति को दर्शाता है जिसमें भाव मुख्य होता है और कर्ता और कर्म गौण होते हैं।</span></p>
          </div>
          <div><b class="color-content9" ><b class="color-content9">उदाहरण-</b></b><br></div>
          <div>
              <ul>
                  <li><span style="background-color: var(--background-color); color: var(--text-color);">मुझे भूख लग रही है। (यहाँ स्थिति "भूख लगना" मुख्य है)</span></li>
                  <li>उसे नींद आ रही है। (यहाँ स्थिति "नींद आना" मुख्य है)</li>
                  <li>हमें ठंड लग रही है। (यहाँ स्थिति "ठंड लगना" मुख्य है)</li>
              </ul>
          </div>
          <div>इन वाक्यों में "<b class="color-content6" >भूख लग रही है</b><span style="background-color: var(--background-color); color: var(--text-color);">," "</span><b class="color-content6" >नींद आ रही है</b><span style="color: var(--text-color); background-color: var(--background-color);">," और "</span><b class="color-content6" >ठंड लग रही है</b><span style="background-color: var(--background-color); color: var(--text-color);">" क्रियाएं भाववाच्य हैं क्योंकि इनमें मुख्यता भाव या स्थिति पर है और कर्ता या कर्म गौण हैं।<br><br></span></div>
          <div>
              <div class="orange-background">वाच्य के आधार पर वाक्यों को तीन भागों में विभाजित किया जाता है: कर्मवाच्य, जिसमें कर्म प्रमुख होता है; कर्तृवाच्य, जिसमें कर्ता प्रमुख होता है; और भाववाच्य, जिसमें भाव या स्थिति प्रमुख होती है। इन वाच्यों का उपयोग करके हम वाक्य में किस तत्व को अधिक महत्व देना है, यह निर्धारित कर सकते हैं।</div>
          </div>
          <div><br></div>
          <div><br></div>
          <div>
              <h2 id="क्रिया के रूप:"><b class="color-content1">क्रिया के रूप</b></h2>
          </div>
          <div><br></div>
          <div>क्रिया के कई रूप होते हैं जो विभिन्न प्रयोजनों और संदर्भों के लिए उपयोग किए जाते हैं। इन रूपों का उपयोग क्रिया की स्थिति, काल, और प्रकार को स्पष्ट करने के लिए किया जाता है। क्रिया के प्रमुख रूप निम्नलिखित हैं:</div>
          <div><br></div>
          <div>
              <p id="1. मूल रूप (Root Form):"><b class="color-content8">1. मूल रूप (Root Form):&nbsp;</b><span style="text-align: left; background-color: var(--background-color); color: var(--text-color);">मूल रूप क्रिया का सबसे सरल और आधारभूत रूप होता है, जिसमें कोई प्रत्यय नहीं होता है। यह रूप क्रिया के मूल अर्थ को दर्शाता है और यह किसी भी परिवर्तन या संशोधन के बिना होता है।</span></p>
          </div>
          <div><b class="color-content9" ><b class="color-content9">उदाहरण-</b></b><br></div>
          <div>
              <ul>
                  <li><span style="background-color: var(--background-color); color: var(--text-color);">पढ़ (पढ़ना क्रिया का मूल रूप)</span></li>
                  <li>लिख (लिखना क्रिया का मूल रूप)</li>
                  <li>खा (खाना क्रिया का मूल रूप)</li>
              </ul>
          </div>
          <div>इन वाक्यों में "<b class="color-content6" >पढ़,</b><span style="background-color: var(--background-color); color: var(--text-color);">" "</span><b class="color-content6" >लिख</b><span style="background-color: var(--background-color); color: var(--text-color);">," और "</span><b class="color-content6" >खा</b><span style="background-color: var(--background-color); color: var(--text-color);">" क्रियाओं के मूल रूप हैं जो बिना किसी प्रत्यय के अपनी सरलतम स्थिति में हैं।</span><br></div>
          <div><br></div>
          <div>
              <p id="2. सामान्य रूप (Infinitive Form):"><b class="color-content8">2. सामान्य रूप (Infinitive Form):&nbsp;</b><span style="text-align: left; background-color: var(--background-color); color: var(--text-color);">सामान्य रूप क्रिया का वह रूप होता है जो मूल रूप में "ना" प्रत्यय जोड़कर बनता है। यह रूप क्रिया के सामान्य और स्थायी रूप को दर्शाता है और अक्सर इसका उपयोग क्रिया के अनिर्धारित या सामान्य रूप में किया जाता है।</span></p>
          </div>
          <div><b class="color-content9" ><b class="color-content9">उदाहरण-</b></b><br></div>
          <div>
              <ul>
                  <li><span style="background-color: var(--background-color); color: var(--text-color);">पढ़ना (मूल रूप "पढ़" + प्रत्यय "ना")</span></li>
                  <li>लिखना (मूल रूप "लिख" + प्रत्यय "ना")</li>
                  <li>खाना (मूल रूप "खा" + प्रत्यय "ना")</li>
              </ul>
          </div>
          <div>इन वाक्यों में "<b class="color-content6" >पढ़ना</b><span style="background-color: var(--background-color); color: var(--text-color);">," "</span><b class="color-content6" >लिखना</b><span style="background-color: var(--background-color); color: var(--text-color);">," और "</span><b class="color-content6" >खाना</b><span style="background-color: var(--background-color); color: var(--text-color);">" सामान्य रूप की क्रियाएं हैं जो क्रिया के अनिर्धारित या सामान्य रूप को दर्शाती हैं।</span><br></div>
          <div><br></div>
          <div>
              <p id="3. विशेषण रूप (Adjectival Form):"><b class="color-content8">3. विशेषण रूप (Adjectival Form):&nbsp;</b><span style="text-align: left; background-color: var(--background-color); color: var(--text-color);">विशेषण रूप क्रिया का वह रूप होता है जो क्रिया में "ता" या "ना" प्रत्यय जोड़कर बनता है। यह रूप क्रिया को विशेषण के रूप में प्रस्तुत करता है, जिससे यह किसी व्यक्ति, वस्तु, या स्थिति की विशेषता को दर्शाता है।</span></p>
          </div>
          <div><b class="color-content9" ><b class="color-content9">उदाहरण-</b></b><br></div>
          <div>
              <ul>
                  <li><span style="background-color: var(--background-color); color: var(--text-color);">पढ़ने योग्य (मूल रूप "पढ़" + प्रत्यय "ने" + योग्य)</span></li>
                  <li>लिखने लायक (मूल रूप "लिख" + प्रत्यय "ने" + लायक)</li>
                  <li>खाने योग्य (मूल रूप "खा" + प्रत्यय "ने" + योग्य)</li>
              </ul>
          </div>
          <div>इन वाक्यों में "<b class="color-content9">पढ़ने योग्य</b>," "<b class="color-content9">लिखने लायक</b>," और "<b class="color-content9">खाने योग्य</b>" विशेषण रूप की क्रियाएं हैं जो किसी कार्य की क्षमता या योग्यता को दर्शाती हैं।</div>
          <div><br></div>
          <div>
              <p id="4. अव्यय रूप (Adverbial Form):"><b class="color-content8">4. अव्यय रूप (Adverbial Form):</b></p>
          </div>
          <div>अव्यय रूप क्रिया का वह रूप होता है जो क्रिया में "कर" या "करके" प्रत्यय जोड़कर बनता है। यह रूप क्रिया को अव्यय के रूप में प्रस्तुत करता है, जिससे यह कार्य के प्रकार, समय, स्थान, या स्थिति को दर्शाता है।</div>
          <div><br></div>
          <div><b class="color-content9"><b class="color-content9">उदाहरण-</b></b></div>
          <div>
              <ul>
                  <li><span style="background-color: var(--background-color); color: var(--text-color);">पढ़कर (मूल रूप "पढ़" + प्रत्यय "कर")</span></li>
                  <li>लिखकर (मूल रूप "लिख" + प्रत्यय "कर")</li>
                  <li>खाकर (मूल रूप "खा" + प्रत्यय "कर")</li>
              </ul>
          </div>
          <div>इन वाक्यों में "<b class="color-content6" >पढ़कर</b><span style="background-color: var(--background-color); color: var(--text-color);">," "</span><b class="color-content6" >लिखकर</b><span style="background-color: var(--background-color); color: var(--text-color);">," और "</span><b class="color-content6" >खाकर</b><span style="background-color: var(--background-color); color: var(--text-color);">" अव्यय रूप की क्रियाएं हैं जो कार्य के प्रकार, समय, या स्थिति को दर्शाती हैं।</span><br></div>
          <div><br></div>
          <div>
              <div class="yellow-background">क्रिया के विभिन्न रूप उनके उपयोग और संदर्भ के आधार पर भिन्न-भिन्न होते हैं। मूल रूप क्रिया का सबसे सरल और आधारभूत रूप होता है, जबकि सामान्य रूप क्रिया के अनिर्धारित या सामान्य रूप को दर्शाता है। विशेषण रूप क्रिया को विशेषण के रूप में प्रस्तुत करता है, और अव्यय रूप क्रिया को अव्यय के रूप में प्रस्तुत करता है। इन रूपों का उपयोग करके हम क्रिया की स्थिति, काल, और प्रकार को स्पष्ट कर सकते हैं।</div>
          </div>
          <div><br></div>
          <div><br></div>
          <div>
              <h2 id="क्रिया के प्रयोग:"><b class="color-content1">क्रिया के प्रयोग</b></h2>
          </div>
          <div><br></div>
          <div>क्रिया का प्रयोग वाक्य में विभिन्न प्रकार से किया जाता है, जो वाक्य की संरचना और अर्थ को स्पष्ट करने में सहायक होता है। क्रिया के प्रयोग के प्रमुख रूप निम्नलिखित हैं:</div>
          <div><br></div>
          <div>
              <p id="1. मुख्य क्रिया (Main Verb):"><b class="color-content7">1. मुख्य क्रिया (Main Verb):</b></p>
          </div>
          <div>मुख्य क्रिया वह क्रिया होती है जो वाक्य का मुख्य अर्थ प्रदान करती है। यह क्रिया वाक्य का केंद्रीय तत्व होती है और इसके बिना वाक्य का अर्थ स्पष्ट नहीं हो पाता। मुख्य क्रिया वाक्य के कर्ता द्वारा किए जा रहे मुख्य कार्य को दर्शाती है।</div>
          <div><b class="color-content9" ><b class="color-content9">उदाहरण-</b></b><br></div>
          <div>
              <ul>
                  <li><span style="background-color: var(--background-color); color: var(--text-color);">मोहन पुस्तक पढ़ रहा है। (यहाँ "पढ़ रहा है" मुख्य क्रिया है)</span></li>
                  <li>राधा खाना बना रही है। (यहाँ "बना रही है" मुख्य क्रिया है)</li>
                  <li>राम खेल रहा है। (यहाँ "खेल रहा है" मुख्य क्रिया है)</li>
              </ul>
          </div>
          <div>इन वाक्यों में "<b class="color-content6" >पढ़ रहा है</b><span style="background-color: var(--background-color); color: var(--text-color);">," "</span><b class="color-content6" >बना रही है</b><span style="background-color: var(--background-color); color: var(--text-color);">," और "</span><b class="color-content6" >खेल रहा है</b><span style="background-color: var(--background-color); color: var(--text-color);">" मुख्य क्रियाएं हैं जो वाक्य के मुख्य अर्थ को स्पष्ट कर रही हैं।</span><br></div>
          <div><br></div>
          <div>
              <p id="2. सहायक क्रिया (Auxiliary Verb):"><b class="color-content7">2. सहायक क्रिया (Auxiliary Verb):</b></p>
          </div>
          <div>सहायक क्रिया वह क्रिया होती है जो मुख्य क्रिया को बनाने में सहायता करती है। यह क्रिया मुख्य क्रिया के साथ मिलकर वाक्य को समय, प्रकार, या स्थिति के अनुसार स्पष्ट करती है। सहायक क्रिया वाक्य में मुख्य क्रिया के साथ प्रयोग होकर उसके अर्थ को पूर्णता प्रदान करती है।</div>
          <div><b class="color-content9" ><b class="color-content9">उदाहरण-</b></b><br></div>
          <div>
              <ul>
                  <li><span style="background-color: var(--background-color); color: var(--text-color);">मैंने पुस्तक पढ़ी है। (यहाँ "है" सहायक क्रिया है जो "पढ़ी" मुख्य क्रिया को पूरा कर रही है)</span></li>
                  <li>वह जा रहा है। (यहाँ "रहा है" सहायक क्रिया है जो "जा" मुख्य क्रिया को पूरा कर रही है)</li>
                  <li>हम काम कर चुके हैं। (यहाँ "चुके हैं" सहायक क्रिया है जो "कर" मुख्य क्रिया को पूरा कर रही है)</li>
              </ul>
          </div>
          <div>इन वाक्यों में "<b class="color-content6" >है</b><span style="background-color: var(--background-color); color: var(--text-color);">," "</span><b class="color-content6" >रहा है</b><span style="background-color: var(--background-color); color: var(--text-color);">," और "</span><b class="color-content6" >चुके हैं</b><span style="background-color: var(--background-color); color: var(--text-color);">" सहायक क्रियाएं हैं जो मुख्य क्रिया के साथ मिलकर वाक्य का अर्थ स्पष्ट कर रही हैं।</span><br></div>
          <div><br></div>
          <div>
              <p id="3. पूर्वकालिक क्रिया (Preceding Action Verb):"><b class="color-content7">3. पूर्वकालिक क्रिया (Preceding Action Verb):</b></p>
          </div>
          <div>पूर्वकालिक क्रिया वह क्रिया होती है जो मुख्य क्रिया के पहले होती है। यह क्रिया उस कार्य को दर्शाती है जो मुख्य क्रिया से पहले संपन्न हुआ हो। पूर्वकालिक क्रिया वाक्य में क्रियाओं के क्रम को स्पष्ट करती है।</div>
          <div><b class="color-content9" ><b class="color-content9">उदाहरण-</b></b><br></div>
          <div>
              <ul>
                  <li><span style="background-color: var(--background-color); color: var(--text-color);">खाना खाकर वह सोने गया। (यहाँ "खाना खाकर" पूर्वकालिक क्रिया है जो "सोने गया" मुख्य क्रिया से पहले हुई)</span></li>
                  <li>पत्र लिखकर उसने उसे भेज दिया। (यहाँ "पत्र लिखकर" पूर्वकालिक क्रिया है जो "भेज दिया" मुख्य क्रिया से पहले हुई)</li>
                  <li>नाश्ता करके वह काम पर चला गया। (यहाँ "नाश्ता करके" पूर्वकालिक क्रिया है जो "काम पर चला गया" मुख्य क्रिया से पहले हुई)</li>
              </ul>
          </div>
          <div>इन वाक्यों में "<b class="color-content9">खाना खाकर</b>," "<b class="color-content9">पत्र लिखकर</b>," और "<b class="color-content9">नाश्ता करके</b>" पूर्वकालिक क्रियाएं हैं जो मुख्य क्रिया से पहले हुई क्रियाओं को दर्शाती हैं।</div>
          <div><br></div>
          <div>
              <p id="4. उत्तरकालिक क्रिया (Subsequent Action Verb):"><b class="color-content7">4. उत्तरकालिक क्रिया (Subsequent Action Verb):</b></p>
          </div>
          <div>उत्तरकालिक क्रिया वह क्रिया होती है जो मुख्य क्रिया के बाद होती है। यह क्रिया उस कार्य को दर्शाती है जो मुख्य क्रिया के बाद संपन्न हुआ हो। उत्तरकालिक क्रिया वाक्य में क्रियाओं के क्रम को स्पष्ट करती है।</div>
          <div><br></div>
          <div><b class="color-content9"><b class="color-content9">उदाहरण-</b></b></div>
          <div>
              <ul>
                  <li><span style="background-color: var(--background-color); color: var(--text-color);">पढ़ाई खत्म करके वह खेलने गया। (यहाँ "पढ़ाई खत्म करके" मुख्य क्रिया है और "खेलने गया" उत्तरकालिक क्रिया है)</span></li>
                  <li>काम निपटाकर वह आराम करने लगा। (यहाँ "काम निपटाकर" मुख्य क्रिया है और "आराम करने लगा" उत्तरकालिक क्रिया है)</li>
                  <li>खाना खाकर उसने टीवी देखा। (यहाँ "खाना खाकर" मुख्य क्रिया है और "टीवी देखा" उत्तरकालिक क्रिया है)</li>
              </ul>
          </div>
          <div>इन वाक्यों में "<b class="color-content9">पढ़ाई खत्म करके</b>," "<b class="color-content9">काम निपटाकर</b>," और "<b class="color-content9">खाना खाकर</b>" उत्तरकालिक क्रियाएं हैं जो मुख्य क्रिया के बाद हुई क्रियाओं को दर्शाती हैं।</div>
          <div><br></div>
          <div>
              <div class="blue1-background">क्रिया का प्रयोग वाक्य में विभिन्न प्रकार से किया जाता है, जो वाक्य की संरचना और अर्थ को स्पष्ट करने में सहायक होता है। मुख्य क्रिया वाक्य का मुख्य अर्थ प्रदान करती है, सहायक क्रिया मुख्य क्रिया को समय, प्रकार, या स्थिति के अनुसार स्पष्ट करती है, पूर्वकालिक क्रिया मुख्य क्रिया से पहले की क्रिया को दर्शाती है, और उत्तरकालिक क्रिया मुख्य क्रिया के बाद की क्रिया को दर्शाती है। इन प्रयोगों का उपयोग करके हम वाक्य को अधिक सटीक और स्पष्ट बना सकते हैं।</div>
          </div>
          <div><br>
              <h2 id="क्रिया के अन्य महत्वपूर्ण पहलू"><b class="color-content1">क्रिया के अन्य महत्वपूर्ण पहलू</b></h2>
          </div>
          <div><br></div>
          <div>क्रिया, वाक्य का एक अनिवार्य तत्व है, जो न केवल कार्य या घटना को दर्शाती है बल्कि कर्ता और अन्य पहलुओं के अनुसार बदलती रहती है। यहाँ क्रिया के कुछ महत्वपूर्ण पहलुओं पर चर्चा की जा रही है:</div>
          <div><br></div>
          <div>
              <p id="1. लिंग और वचन (Gender and Number):"><b class="color-content3">1. लिंग और वचन (Gender and Number)</b></p>
          </div>
          <div>क्रिया का रूप वाक्य में कर्ता के लिंग (पुल्लिंग/स्त्रीलिंग) और वचन (एकवचन/बहुवचन) के अनुसार बदलता है। इससे वाक्य का अर्थ स्पष्ट होता है और यह दर्शाता है कि क्रिया किस प्रकार की है और कौन इसे कर रहा है।</div>
          <div><b class="color-content9" ><b class="color-content9">उदाहरण-</b></b><br></div>
          <div>
              <ul>
                  <li><span style="background-color: var(--background-color); color: var(--text-color);">वह लड़का दौड़ रहा है। (यहाँ "दौड़ रहा है" पुल्लिंग और एकवचन है, जो एक लड़के द्वारा किए जा रहे कार्य को दर्शाता है)</span></li>
                  <li>वे लड़कियाँ दौड़ रही हैं। (यहाँ "दौड़ रही हैं" स्त्रीलिंग और बहुवचन है, जो कई लड़कियों द्वारा किए जा रहे कार्य को दर्शाता है)</li>
              </ul>
          </div>
          <div>इन वाक्यों में क्रिया का रूप कर्ता के लिंग और वचन के अनुसार बदल रहा है, जिससे वाक्य का अर्थ स्पष्ट होता है।</div>
          <div><br></div>
          <div>
              <p id="2. प्रेरणार्थक क्रिया (Causative Verb):"><b class="color-content3">2. प्रेरणार्थक क्रिया (Causative Verb):</b></p>
          </div>
          <div>प्रेरणार्थक क्रिया वह क्रिया होती है जो किसी को कोई कार्य करने के लिए प्रेरित करती है। यह क्रिया यह दर्शाती है कि कर्ता किसी अन्य व्यक्ति को किसी कार्य के लिए प्रेरित कर रहा है।</div>
          <div><b class="color-content9" ><b class="color-content9">उदाहरण-</b></b><br></div>
          <div>
              <ul>
                  <li><span style="background-color: var(--background-color); color: var(--text-color);">माँ बेटे को पढ़ने के लिए कहती हैं। (यहाँ "कहती हैं" प्रेरणार्थक क्रिया है, जो माँ द्वारा बेटे को पढ़ने के लिए प्रेरित करने का कार्य दर्शा रही है)</span></li>
                  <li>शिक्षक छात्रों को अभ्यास करने के लिए प्रेरित करते हैं। (यहाँ "प्रेरित करते हैं" प्रेरणार्थक क्रिया है, जो शिक्षक द्वारा छात्रों को अभ्यास करने के लिए प्रेरित करने का कार्य दर्शा रही है)</li>
              </ul>
          </div>
          <div>इन वाक्यों में "<b class="color-content9">कहती हैं</b>" और "<b class="color-content9">प्रेरित करते हैं</b>" प्रेरणार्थक क्रियाएं हैं जो कर्ता द्वारा किसी को कार्य करने के लिए प्रेरित करने का भाव व्यक्त करती हैं।</div>
          <div><br></div>
          <div>
              <p id="3. निषेधात्मक क्रिया (Negative Verb):"><b class="color-content3">3. निषेधात्मक क्रिया (Negative Verb):</b></p>
          </div>
          <div>निषेधात्मक क्रिया वह क्रिया होती है जो किसी कार्य को न करने का बोध कराती है। यह क्रिया वाक्य में निषेध या रोक का भाव व्यक्त करती है।</div>
          <div><b class="color-content9" ><b class="color-content9">उदाहरण-</b></b><br></div>
          <div><br></div>
          <div>
              <ul>
                  <li>बच्चे बाहर नहीं खेल रहे हैं। (यहाँ "नहीं खेल रहे हैं" निषेधात्मक क्रिया है, जो यह दर्शा रही है कि बच्चे बाहर खेल नहीं रहे हैं)</li>
                  <li>राम ने आज काम नहीं किया। (यहाँ "नहीं किया" निषेधात्मक क्रिया है, जो यह दर्शा रही है कि राम ने आज काम नहीं किया)</li>
              </ul>
          </div>
          <div>इन वाक्यों में "<b class="color-content9">नहीं खेल रहे हैं</b>" और "<b class="color-content9">नहीं किया</b>" निषेधात्मक क्रियाएं हैं जो कार्य के निषेध या रोक को व्यक्त करती हैं।</div>
          <div><br></div>
          <div>
              <p id="4. क्रियाओं का समूह (Verb Group):"><b class="color-content3">4. क्रियाओं का समूह (Verb Group):</b></p>
          </div>
          <div>कभी-कभी एक वाक्य में एक से अधिक क्रियाओं का प्रयोग किया जाता है, जिन्हें क्रिया समूह के रूप में जाना जाता है। क्रिया समूह मिलकर वाक्य को पूरा अर्थ प्रदान करते हैं।</div>
          <div><b class="color-content9" ><b class="color-content9">उदाहरण-</b></b><br></div>
          <div>
              <ul>
                  <li><span style="background-color: var(--background-color); color: var(--text-color);">मोहन पुस्तक पढ़कर सो गया। (यहाँ "पढ़कर" और "सो गया" क्रिया समूह बनाते हैं)</span></li>
                  <li>सीता खाना बनाकर खा रही है। (यहाँ "बनाकर" और "खा रही है" क्रिया समूह बनाते हैं)</li>
                  <li>राम दौड़कर घर आ गया। (यहाँ "दौड़कर" और "आ गया" क्रिया समूह बनाते हैं)</li>
              </ul>
          </div>
          <div>इन वाक्यों में "<b class="color-content9">पढ़कर</b>" और "<b class="color-content9">सो गया</b>," "<b class="color-content9">बनाकर</b>" और "<b class="color-content9">खा रही है</b>," और "<b class="color-content9">दौड़कर</b>" और "<b class="color-content9">आ गया</b>" क्रिया समूह बनाते हैं जो वाक्य को पूरा अर्थ प्रदान करते हैं।</div>

