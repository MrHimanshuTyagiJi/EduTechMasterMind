              <p id="2. भूतकाल (Past Tense):"><b class="color-content8">2. भूतकाल (Past Tense):&nbsp;</b><span style="text-align: left; background-color: var(--background-color); color: var(--text-color);">भूतकाल की क्रिया वह होती है जो पहले हो चुकी है। यह क्रिया उस घटना या कार्य को दर्शाती है जो पहले संपन्न हो चुकी है। यह समय के उस पल को दिखाती है जब क्रिया पूरी हो चुकी थी।</span></p>
