              <ul>
                  <li><span style="background-color: var(--background-color); color: var(--text-color);">चिट्ठी लिखी गई है। (यहाँ "चिट्ठी" कर्म है और इसे प्रमुखता दी गई है)</span></li>
                  <li>खाना खाया गया। (यहाँ "खाना" कर्म है और इसे प्रमुखता दी गई है)</li>
                  <li>पुस्तक पढ़ी गई। (यहाँ "पुस्तक" कर्म है और इसे प्रमुखता दी गई है)</li>
