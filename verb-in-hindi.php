          <div>&nbsp;<br><b class="color-content5" >कर्म</b><b class="color-content5" >&nbsp;के आधार पर क्रियाएं-<br></b><br><b class="color-content8" >सकर्मक क्रिया (Transitive Verb):</b><b class="color-content6" >&nbsp;</b><span style="color: var(--text-color); background-color: var(--background-color);">सकर्मक क्रिया वह क्रिया होती है जिसमें क्रिया का कार्य किसी वस्तु या व्यक्ति पर होता है, जिसे कर्म कहा जाता है। सकर्मक क्रिया में कार्य के निष्पादन के लिए किसी कर्म की आवश्यकता होती है। इस प्रकार की क्रिया में क्रिया का परिणाम स्पष्ट रूप से किसी न किसी पर पड़ता है, यानी कर्म पर।</span><br></div>
          <div><b class="color-content9" ><b class="color-content9">उदाहरण-</b></b><br></div>
          <div>
              <ul>
                  <li><span style="background-color: var(--background-color); color: var(--text-color);">मोहन पुस्तक पढ़ रहा है। (यहाँ "पुस्तक" कर्म है)</span></li>
                  <li>राधा खाना बना रही है। (यहाँ "खाना" कर्म है)</li>
                  <li>राम ने चिट्ठी लिखी। (यहाँ "चिट्ठी" कर्म है)</li>
              </ul>
          </div>
          <div>इन वाक्यों में, "<b class="color-content6" >पुस्तक</b><span style="background-color: var(--background-color); color: var(--text-color);">," "</span><b class="color-content6" >खाना</b><span style="background-color: var(--background-color); color: var(--text-color);">," और "</span><b class="color-content6" >चिट्ठी</b><span style="background-color: var(--background-color); color: var(--text-color);">" क्रमशः क्रियाओं "</span><b class="color-content6" >पढ़ रहा है</b><span style="background-color: var(--background-color); color: var(--text-color);">," "</span><b class="color-content6" >बना रही है</b><span style="background-color: var(--background-color); color: var(--text-color);">," और "</span><b class="color-content6" >लिखी</b><span style="background-color: var(--background-color); color: var(--text-color);">" के कर्म हैं। ये सभी वाक्य सकर्मक क्रिया के&nbsp;</span><b class="color-content9">उदाहरण</b><span style="background-color: var(--background-color); color: var(--text-color);">&nbsp;हैं क्योंकि इन क्रियाओं का कार्य किसी न किसी वस्तु या व्यक्ति पर हो रहा है।</span><br></div>
          <div><br></div>
          <div>
              <p id="अकर्मक क्रिया (Intransitive Verb):"><b class="color-content8">अकर्मक क्रिया (Intransitive Verb):&nbsp;</b><span style="background-color: var(--background-color); color: var(--text-color); text-align: left;">अकर्मक क्रिया वह क्रिया होती है जिसमें क्रिया का कार्य किसी वस्तु या व्यक्ति पर नहीं होता है। दूसरे शब्दों में, यह क्रिया कर्म के बिना ही पूरी होती है। अकर्मक क्रिया में केवल कर्ता का ही उल्लेख होता है, और कोई कर्म नहीं होता।<br></span><b class="color-content9" ><b class="color-content9">उदाहरण-<br></b></b></p>
