          <div><br></div>
          <div>
              <p id="2. कर्तृवाच्य (Active Voice):"><b class="color-content8">2. कर्तृवाच्य (Active Voice):&nbsp;</b><span style="text-align: left; background-color: var(--background-color); color: var(--text-color);">कर्तृवाच्य वह वाक्य होता है जिसमें कर्ता पर अधिक महत्व दिया जाता है। इस वाक्य में कार्य करने वाला व्यक्ति या वस्तु, यानी कर्ता, प्रमुख होता है और क्रिया उसके द्वारा संपन्न होती है। यह वाक्य उस स्थिति को दर्शाता है जिसमें कर्ता मुख्य होता है और कार्य उसके द्वारा किया जा रहा होता है।<br></span><b class="color-content9" ><b class="color-content9">उदाहरण-</b></b></p>
